pdfjsLib.GlobalWorkerOptions.workerSrc = 'https://cdnjs.cloudflare.com/ajax/libs/pdf.js/3.11.174/pdf.worker.min.js';

let activeEifpFile = null;
PdfDevPro.buildDropZone('eifp-drop-zone', {accept: 'application/pdf', title: 'PDF document', icon: '🔍', onFiles: files => {
    activeEifpFile = files[0];
    document.getElementById('eifp-controls').style.display = 'block';
}});

async function extractNativeImages() {
    if(!activeEifpFile) return;
    const status = document.getElementById('eifp-status');
    const resDiv = document.getElementById('eifp-results');
    status.style.display = 'block';
    resDiv.innerHTML = '';
    
    try {
        status.textContent = 'Forensically scanning PDF operators...';
        const fileBuffer = await PdfDevPro.readFileAsArrayBuffer(activeEifpFile);
        const pdf = await pdfjsLib.getDocument({ data: fileBuffer }).promise;
        const total = pdf.numPages;
        
        let imgCount = 0;
        
        for(let i=1; i<=total; i++) {
            status.textContent = `Scanning Page ${i} / ${total}...`;
            const page = await pdf.getPage(i);
            const opList = await page.getOperatorList();
            
            // Loop through operators looking for images
            for(let j=0; j < opList.fnArray.length; j++) {
                // In pdf.js paintImageXObject is code 85 or handled via specific fns
                if (opList.fnArray[j] === pdfjsLib.OPS.paintImageXObject || opList.fnArray[j] === pdfjsLib.OPS.paintJpegXObject) {
                    const objId = opList.argsArray[j][0];
                    try {
                        const img = await page.objs.get(objId);
                        if (img && img.data && img.width && img.height) {
                            imgCount++;
                            const canvas = document.createElement('canvas');
                            canvas.width = img.width;
                            canvas.height = img.height;
                            const ctx = canvas.getContext('2d');
                            // Create ImageData
                            const imgData = new ImageData(new Uint8ClampedArray(img.data), img.width, img.height);
                            ctx.putImageData(imgData, 0, 0);
                            
                            const dataUrl = canvas.toDataURL('image/png');
                            resDiv.innerHTML += `
                            <div class="col-md-3 text-center">
                                <div class="border rounded-3 p-2 bg-white shadow-sm h-100 d-flex flex-column align-items-center justify-content-between">
                                    <div style="background:var(--bg-body); padding:10px; border-radius:8px; width:100%; display:flex; justify-content:center; align-items:center; min-height:120px;">
                                        <img src="${dataUrl}" style="max-width:100%; max-height:100px;">
                                    </div>
                                    <div class="w-100 mt-2">
                                        <div class="text-muted small fw-bold mb-1">Pg ${i} · ${img.width}x${img.height}</div>
                                        <a href="${dataUrl}" download="extracted_asset_${imgCount}.png" class="btn btn-sm btn-outline-primary w-100">Save Asset</a>
                                    </div>
                                </div>
                            </div>`;
                        }
                    } catch(ex) { console.log('Failed to parse object: ' + objId); }
                }
            }
        }
        
        if (imgCount === 0) {
            status.innerHTML = `<span class="text-warning">⚠️ No graphical raster assets found embedded in this document.</span>`;
        } else {
            status.innerHTML = `<span class="text-success">✅ Successfully isolated ${imgCount} native raster images!</span>`;
            PdfDevPro.toast(`Found ${imgCount} images!`);
        }
        
    } catch(e) {
        status.innerHTML = `<span class="text-danger">Error Context: ${e.message}</span>`;
    }
}
</script>






