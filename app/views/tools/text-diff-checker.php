<script>
document.addEventListener('DOMContentLoaded', () => {

    const dmp = new diff_match_patch();
    const tLeft = document.getElementById('text-left');
    const tRight = document.getElementById('text-right');
    const container = document.getElementById('diff-output-container');
    const content = document.getElementById('diff-content');

    window.diffCheck = () => {
        const text1 = tLeft.value;
        const text2 = tRight.value;

        if (!text1 && !text2) {
            container.classList.add('d-none');
            return;
        }

        container.classList.remove('d-none');

        // Allow UI to unfreeze
        setTimeout(() => {
            const diffs = dmp.diff_main(text1, text2);
            dmp.diff_cleanupSemantic(diffs); // Makes the diffs much more human readable!
            
            const htmlStream = dmp.diff_prettyHtml(diffs);
            
            // The default library uses inline styles <del style="..."> and <ins style="...">. 
            // We strip their inline colors to use our custom Bootstrap-friendly CSS defined above.
            const cleanHtml = htmlStream
                .replace(/style="background:#e6ffe6;"/g, '')
                .replace(/style="background:#ffe6e6;"/g, '');

            content.innerHTML = cleanHtml;
            
        }, 50);
    };

    // Load samples
    window.loadSamples = () => {
        tLeft.value = "The quick brown fox jumps over the lazy dog.\n\nThis tool is awesome.";
        tRight.value = "The fast brown fox jumps over the sleepy dog.\n\nThis Pro tool is truly awesome.";
        diffCheck();
    };
});
</script>






