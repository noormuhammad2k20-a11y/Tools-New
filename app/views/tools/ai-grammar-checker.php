<script>
    document.addEventListener('DOMContentLoaded', () => {
        const btn = document.getElementById('gen-btn');
        const loading = document.getElementById('ai-loading');
        const ui = document.getElementById('tool-ui');
        const resultWrapper = document.getElementById('result-wrapper');
        const correctedBox = document.getElementById('corrected-result');
        const matchesBox = document.getElementById('matches-result');
        const textInput = document.getElementById('text-input');

        btn.addEventListener('click', async () => {
            const text = textInput.value.trim();
            if(!text) return;

            loading.style.display = 'block';
            ui.style.display = 'none';
            resultWrapper.style.display = 'none';

            try {
                const res = await AITools.checkGrammarAndTone(text);
                correctedBox.innerText = res.corrected;
                
                if (res.matches.length === 0) {
                    matchesBox.innerHTML = '<div class="alert alert-success py-2">No errors found! Great job.</div>';
                } else {
                    matchesBox.innerHTML = res.matches.slice(0, 10).map(m => `
                        <div class="mb-3 pb-2 border-bottom">
                            <div class="fw-bold text-danger">${m.message}</div>
                            <div class="text-muted small mt-1">Found: "${text.substring(m.offset, m.offset + m.length)}"</div>
                            ${m.replacements.length > 0 ? `<div class="mt-1">Suggested: <span class="badge bg-success">${m.replacements[0].value}</span></div>` : ''}
                        </div>
                    `).join('');
                }

                loading.style.display = 'none';
                resultWrapper.style.display = 'block';
                resultWrapper.scrollIntoView({ behavior: 'smooth' });
                ui.style.display = 'block';
            } catch (error) {
                console.error(error);
                alert('Connection error or API limit reached. Using direct LanguageTool API.');
                loading.style.display = 'none';
                ui.style.display = 'block';
            }
        });

        document.getElementById('copy-btn').addEventListener('click', () => {
            navigator.clipboard.writeText(correctedBox.innerText);
            alert('Corrected text copied!');
        });
    });
</script>






